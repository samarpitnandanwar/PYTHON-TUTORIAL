a = 3
b = 4

# Arithmetic Operators
print("Value of 3+4 is ", a+b)
print("Value of 3*4 is ", a*b)
print("Value of 3/4 is ", a/b)
print("Value of 3-4 is ", a-b)

# Assignment Operators
a += b # same as a = a + b
b -= a # same as b = b - a
a *= b # same as a = a * b
a /= b # same as a = a / b
print(a)


# Comparison Operators
c = (14<=7)
print(c)
d = (14>=7)
print(d)
e = (14>7)
print(e)
f = (14<7)
print(f)
g = (14==7)
print(g)
h = (14!=7)
print(h)


# Logical Operators

bool1 = True
bool2 = False
print(bool1 and bool2)
print(bool1 or bool2)
print(not bool1)
